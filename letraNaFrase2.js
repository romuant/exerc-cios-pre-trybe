//quantas veses uma letra aparece na frase
 function vesesLetraAparece(frase,letra) {
     let quantidade = 0
     const getLetrasNaFrase = (letrasNaFrase)=> {
        if(letrasNaFrase ===letra)
        
            quantidade ++
        }
    }
    frase.split("").forEach(getLetrasNaFrase)
    return quantidade
     
 


